export const allServices = Object.values(servicesData).flatMap(category =>
  category.services.map(service => ({
    ...service,
    category: category.title,
    categorySlug: category.slug,
    icon: category.icon,
    gradient: category.gradient
  }))
);

export const getCategoryBySlug = (slug) => {
  return Object.values(servicesData).find(category => category.slug === slug);
};

export const getServiceBySlug = (categorySlug, serviceSlug) => {
  const category = getCategoryBySlug(categorySlug);
  if (!category) return null;
  return category.services.find(service => service.slug === serviceSlug);
};
